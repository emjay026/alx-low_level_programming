#include "3-calc.h"

/**
* op_add - function that adds two integers.
* @a: first integer
* @b: second integer
*
* Return: addition of two integers.
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - function that subtracts two integers.
* @a: first integer
* @b: second integer
*
* Return: subtraction of two integers.
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - function that multiplies two integers.
* @a: first integer
* @b: second integer
*
* Return: multiplication of two integers.
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - function that divides two integers.
* @a: first integer
* @b: second integer
*
* Return: division of two integers
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - function that divides two integers and get remainder.
* @a: first integer
* @b: second integer
*
* Return: modulus of two integers.
*/
int op_mod(int a, int b)
{
	return (a % b);
}
